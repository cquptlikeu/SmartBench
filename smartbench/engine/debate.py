"""
Generic multi-model debate engine — no project-specific hardcoding.

Three roles:
1. Proposer — analyzes context and proposes fixes
2. Critique — reviews proposals for risks
3. Judge — synthesizes into final recommendations

All prompts are generated by PromptFactory based on ProjectFingerprint.
"""

import json
import time
from typing import List, Dict, Any, Optional, Callable
from dataclasses import dataclass, field

from smartbench.detector.fingerprint import ProjectFingerprint
from smartbench.prompts.factory import PromptFactory


@dataclass
class DebateResult:
    """Structured result from a debate round."""
    final_suggestions: List[Dict[str, Any]] = field(default_factory=list)
    debate_log: List[Dict[str, Any]] = field(default_factory=list)
    consensus_reached: bool = False
    iterations: int = 0
    total_tokens_used: int = 0
    duration_ms: int = 0


@dataclass
class ModelResponse:
    """Response from a single model call."""
    model_name: str
    role: str
    content: str
    success: bool
    error: Optional[str] = None
    tokens_used: int = 0


class DebateEngine:
    """
    Generic multi-model debate engine.

    Usage:
        factory = PromptFactory(fingerprint)
        engine = DebateEngine(llm_call_fn, factory)
        result = engine.debate(analysis_context, target="Improve performance")
    """

    def __init__(
        self,
        llm_call_fn: Callable[[str], str],
        prompt_factory: Optional[PromptFactory] = None,
        fingerprint: Optional[ProjectFingerprint] = None,
        max_iterations: int = 2,
        timeout_per_call: int = 60,
    ):
        """
        Args:
            llm_call_fn: Function that takes a prompt string and returns response text
            prompt_factory: PromptFactory instance (created from fingerprint)
            fingerprint: ProjectFingerprint (used to create factory if not provided)
            max_iterations: Maximum debate rounds
            timeout_per_call: Timeout per LLM call in seconds
        """
        self.llm_call = llm_call_fn
        # Fix: operator precedence — 'or ... if ... else' binds as (or ...) if ... else
        if prompt_factory is not None:
            self.factory = prompt_factory
        elif fingerprint is not None:
            self.factory = PromptFactory(fingerprint)
        else:
            self.factory = None
        self.max_iterations = max_iterations
        self.timeout = timeout_per_call

    def debate(
        self,
        analysis_context: str,
        target: str = "Identify and fix the most impactful issues",
        model_name: str = "default",
    ) -> DebateResult:
        """
        Run a full debate cycle.

        Args:
            analysis_context: Built by PromptFactory.build_analysis_context()
            target: What we're trying to achieve
            model_name: Name of the model being used (for logging)

        Returns:
            DebateResult with final suggestions
        """
        if not self.factory:
            raise RuntimeError("DebateEngine requires a PromptFactory. "
                             "Pass one to __init__ or provide a fingerprint.")

        start_time = time.time()
        log: List[Dict[str, Any]] = []
        total_chars = 0  # approximate token count: chars / 3

        # ── Round 1: Proposer ─────────────────────────────────────────
        proposer_prompt = self.factory.build_proposer_prompt(
            analysis_context, target,
        )
        proposer_raw = self._safe_call(proposer_prompt, model_name)
        proposer_json = self._parse_json(proposer_raw)
        total_chars += len(proposer_prompt) + len(proposer_raw or "")
        log.append({"role": "proposer", "input": proposer_prompt[:500],
                     "output": proposer_raw[:500] if proposer_raw else ""})

        if not proposer_json:
            return DebateResult(
                debate_log=log,
                final_suggestions=[],
                duration_ms=int((time.time() - start_time) * 1000),
                total_tokens_used=total_chars // 3,
            )

        # ── Round 2: Critique ─────────────────────────────────────────
        critique_prompt = self.factory.build_critique_prompt(
            json.dumps(proposer_json, ensure_ascii=False, indent=2),
            analysis_context,
        )
        critique_raw = self._safe_call(critique_prompt, model_name)
        critique_json = self._parse_json(critique_raw)
        total_chars += len(critique_prompt) + len(critique_raw or "")
        log.append({"role": "critique", "input": critique_prompt[:500],
                     "output": critique_raw[:500] if critique_raw else ""})

        # ── Round 3: Judge ────────────────────────────────────────────
        judge_prompt = self.factory.build_judge_prompt(
            json.dumps(proposer_json, ensure_ascii=False, indent=2),
            json.dumps(critique_json, ensure_ascii=False, indent=2) if critique_json else "{}",
            analysis_context,
        )
        judge_raw = self._safe_call(judge_prompt, model_name)
        judge_json = self._parse_json(judge_raw)
        total_chars += len(judge_prompt) + len(judge_raw or "")
        log.append({"role": "judge", "input": judge_prompt[:500],
                     "output": judge_raw[:500] if judge_raw else ""})

        # ── Extract final suggestions ──────────────────────────────────
        final_suggestions = []
        if judge_json:
            final_suggestions = judge_json.get("final_suggestions", [])
        elif proposer_json:
            # Fallback: use proposer suggestions directly if judge fails
            final_suggestions = proposer_json.get("proposals", [])

        elapsed = int((time.time() - start_time) * 1000)

        return DebateResult(
            final_suggestions=final_suggestions,
            debate_log=log,
            consensus_reached=judge_json is not None,
            iterations=3,
            total_tokens_used=total_chars // 3,
            duration_ms=elapsed,
        )

    # ── Helpers ───────────────────────────────────────────────────────

    def _safe_call(self, prompt: str, model_name: str) -> str:
        """Call LLM with timeout protection."""
        try:
            return self.llm_call(prompt)
        except Exception as e:
            return json.dumps({"error": str(e)})

    @staticmethod
    def _parse_json(raw: str) -> Optional[Dict]:
        """Robust JSON parsing from LLM output."""
        import re

        if not raw:
            return None

        cleaned = raw.strip()

        # Remove markdown code fences
        if cleaned.startswith("```"):
            cleaned = re.sub(r"^```(?:json)?\s*\n?", "", cleaned)
            cleaned = re.sub(r"\n?```\s*$", "", cleaned)

        # Try direct parse
        try:
            return json.loads(cleaned)
        except json.JSONDecodeError:
            pass

        # Try to extract JSON block
        match = re.search(r"\{[\s\S]*\}", cleaned)
        if match:
            try:
                return json.loads(match.group())
            except json.JSONDecodeError:
                pass

        return None
